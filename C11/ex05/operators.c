#include <stdio.h>

int	add(int a, int b)
{
	return (a + b);
}

int	substract(int a, int b)
{
	return (a - b);
}

int	divide(int a, int b)
{
	return (a / b);
}

int	mod(int a, int b)
{
	return (a % b);
}

int	mult(int a, int b)
{
	return (a * b);
}
